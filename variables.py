# ##### Data types ####

# Integer
num_server = 10 
print(num_server) # num_server stores value 10 and prints the same
print(type(num_server))

# we can give mutltiple times of key values pair
num_server = 30 
print(num_server) # num_server stores value 30 and prints the same
print(type(num_server))

# Float
CONST_PI = 89.05 
print(CONST_PI)
print(type(CONST_PI))

# String
server = "Ansible"
print(server)
print(type(server))

# Boolean
is_prod = False
print(is_prod)
print(type(is_prod))


